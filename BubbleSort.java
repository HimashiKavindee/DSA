import java.util.Arrays;

public class BubbleSort{
       public static void main(String[]args){
	   int i=3;
	   int j=4;
	   System.out.println("before swapping");
	   System.out.println( "i: " +i);
	   System.out.println( "j: " +j);
	   
	   int temp=i;
	   i=j;
	   j=temp;
	   
	   System.out.println("after swapping");
	   System.out.println( "i: " +i);
	   System.out.println( "j: " +j);
	   }
}	   

